package ws2021.section_a;

public class EscapeCharacters {

    public static void main(String[] args) {
        System.out.printf("escape sequence is started with a backslash: \\\n");
        System.out.printf("to print a tab (\t) use \"\\t\"\n");
        System.out.printf("to print a backslash (\\) use \"\\\\\"\n");
        System.out.printf("to print double quotes (\") use \"\\\"\"\n");
        System.out.printf("to print a percent sign (%%) use \"%%%%\"\n");
        System.out.printf("to start a new line (\n) use \"\\n\"\n");
    }
}
