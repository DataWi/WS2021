package ws2021.section_a;

public class IntegerArithmeticDemo {

    public static void main(String[] args) {
        System.out.printf("%d + %d = %d\n", 19, 4, 19 + 4);
        System.out.printf("%d - %d = %d\n", 19, 4, 19 - 4);
        System.out.printf("%d * %d = %d\n", 19, 4, 19 * 4);
        System.out.printf("%d / %d = %d\n", 19, 4, 19 / 4);
        System.out.printf("%d %% %d = %d\n", 19, 4, 19 % 4);
    }
}
