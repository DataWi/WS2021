package ws2021.section_a;

public class DoubleArithmeticDemo {

    public static void main(String[] args) {
        System.out.printf("%f + %f = %f\n", 19., 4., 19. + 4.);
        System.out.printf("%f - %f = %f\n", 19., 4., 19. - 4.);
        System.out.printf("%f * %f = %f\n", 19., 4., 19. * 4.);
        System.out.printf("%f / %f = %f\n", 19., 4., 19. / 4.);
        System.out.printf("%f / %f = %f\n", 19., 0., 19. / 0.);
        System.out.printf("%f / %f = %f\n", 0., 0., 0. % 0.);
    }
}
